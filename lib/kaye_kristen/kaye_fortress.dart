class KayeQuicheDame extends NavigatorObserver {
  KayeQuicheDame._internal();

  static final _instance = KayeQuicheDame._internal();

  factory KayeQuicheDame() => _instance;

  static KayeQuicheDame getInstance() {
    return _instance;
  }

  static final List<Route> _appRoutes = [];

  void _addRoute(Route? route) {
    if (route == null) return;
    _appRoutes.add(route);
  }

  static void _removeRoute(Route? route) {
    _appRoutes.remove(route);
  }

  static bool hasPageRoute(String routeName) {
    return _appRoutes
            .where((element) => element.settings.name == routeName)
            .lastOrNull !=
        null;
  }

  static void removeRoute(String routeName) {
    Route? route = _appRoutes
        .where((element) => element.settings.name == routeName)
        .lastOrNull;
    if (route == null) return;
    Get.removeRoute(route);
  }

  @override
  void didPush(Route<dynamic> route, Route<dynamic>? previousRoute) {
    super.didPush(route, previousRoute);
    onPause(previousRoute?.settings.name);
    onResume(route.settings.name);
    _addRoute(route);
  }

  @override
  void didPop(Route<dynamic> route, Route<dynamic>? previousRoute) {
    super.didPop(route, previousRoute);
    onPause(route.settings.name);
    _removeRoute(route);
    onResume(previousRoute?.settings.name);
  }

  @override
  void didRemove(Route<dynamic> route, Route<dynamic>? previousRoute) {
    super.didRemove(route, previousRoute);
    onPause(route.settings.name);
    _removeRoute(route);
    onResume(previousRoute?.settings.name);
  }

  @override
  void didReplace({Route<dynamic>? newRoute, Route<dynamic>? oldRoute}) {
    super.didReplace(newRoute: newRoute, oldRoute: oldRoute);
    onPause(oldRoute?.settings.name);
    _removeRoute(oldRoute);
    onResume(newRoute?.settings.name);
    _addRoute(newRoute);
  }

  @override
  void didStartUserGesture(
    Route<dynamic> route,
    Route<dynamic>? previousRoute,
  ) {
    super.didStartUserGesture(route, previousRoute);
  }

  @override
  void didStopUserGesture() {
    super.didStopUserGesture();
  }

  Map<String, int?> routeOnResumeTime = HashMap();

  void onResume(String? routeName) {
    if (routeName == null) return;
    if (routeName == KayeFortress.KayeInclinePlanner)
      onResume(
        KayeInclineGlorify.findTabRoute(KayeInclineGlorify.currentTabId),
      );

    int? onResumeTime = routeOnResumeTime[routeName];
    if (onResumeTime != null) return;
    onResumeTime = DateTime.now().millisecondsSinceEpoch;
    routeOnResumeTime[routeName] = onResumeTime;
    KAYE.fire(KayePlannerDonna(routeName, KayePlannerDonna.RESUME));
    KayeKristenGlitterFlattering.kayePlayhouseSwimDevelop(routeName);

    if (isPreventScreenshotOffPage(routeName)) {
      KayePoliticalLeadFlattering.instance.kayeMisterLeadSwimFax(false);
    }

    Future.delayed(const Duration(seconds: 1), () {
      if (Get.currentRoute == routeName)
        KayePoliticalLeadFlattering.instance.kayeAmazonCuterSuffice();
    });
  }

  void onPause(String? routeName) {
    if (routeName == null) return;
    if (routeName == KayeFortress.KayeInclinePlanner)
      onPause(KayeInclineGlorify.findTabRoute(KayeInclineGlorify.currentTabId));
    int? onResumeTime = routeOnResumeTime[routeName];
    if (onResumeTime == null) return;
    routeOnResumeTime[routeName] = null;
    int duration = DateTime.now().millisecondsSinceEpoch - onResumeTime;
    KAYE.fire(KayePlannerDonna(routeName, KayePlannerDonna.PAUSE));
    KayeKristenGlitterFlattering.kayePlayhouseSwimTomato(routeName, duration);

    if (isPreventScreenshotOffPage(routeName)) {
      KayePoliticalLeadFlattering.instance.kayeMisterLeadSwimFax(true);
    }
  }

  bool isPreventScreenshotOffPage(String? routeName) {
    if (KayeWrestlingBarnacle.isEmpty(routeName)) {
      return false;
    }
    if (routeName!.contains("me") ||
        routeName!.contains(KayeFortress.KayeAttachPlanner) ||
        routeName.contains(KayeFortress.KayeMittenMePlanner) ||
        routeName.contains(KayeFortress.KayePuddingLagVailPlanner) ||
        routeName.contains(KayeFortress.KayeLagPassengerPlanner) ||
        routeName.contains(KayeFortress.KayeDeliveryVailPlanner) ||
        routeName.contains(KayeFortress.KayeDeliveryPassengerPlanner) ||
        routeName.contains(KayeFortress.KayeSprintManhattanPlanner) ||
        routeName.contains(KayeFortress.KayeCatheterPassengerPlanner) ||
        routeName.contains(KayeFortress.KayePitySprintConcernedPlanner)) {
      return true;
    }
    return false;
  }
}
